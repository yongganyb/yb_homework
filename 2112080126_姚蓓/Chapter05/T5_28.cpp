#include <iostream>

using namespace std;

int main()
{
    for(int i=1;i<=12;i++)
    {
        switch(i)
        {
        case 1:
            cout << "first" << endl;
            break;
        case 2:
            cout << "second" << endl;
            break;
        case 3:
            cout << "third" << endl;
            break;
        case 4:
            cout << "forth" << endl;
            break;
        case 5:
            cout << "fifth" << endl;
            break;
        case 6:
            cout << "sixth" << endl;
            break;
        case 7:
            cout << "seventh" << endl;
            break;
        case 8:
            cout << "eighth" << endl;
            break;
        case 9:
            cout << "ninth" << endl;
            break;
        case 10:
            cout << "tenth" << endl;
            break;
        case 11:
            cout << "eleventh" << endl;
            break;
        case 12:
            cout << "twelfth" << endl;
            break;
        }
    }
    return 0;
}
